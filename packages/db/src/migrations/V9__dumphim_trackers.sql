CREATE TABLE dumphim_trackers (
  id CHAR(36) PRIMARY KEY DEFAULT (UUID()),
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  name VARCHAR(255) NOT NULL,
  hp VARCHAR(255) NULL,
  card_type VARCHAR(50) NULL,
  description TEXT NULL,
  attacks JSON NULL,
  strengths JSON NULL,
  flaws JSON NULL,
  commitment_level VARCHAR(50) NULL,
  color_theme VARCHAR(50) NULL,
  photo_url TEXT NULL,
  image_scale DECIMAL(5, 3) NULL,
  image_position JSON NULL,
  user_id VARCHAR(191) NOT NULL,
  INDEX idx_dumphim_trackers_user_created (user_id, created_at),
  INDEX idx_dumphim_trackers_created (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
